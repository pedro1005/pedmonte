#include "libft.h"
#include <stdio.h>
#include <stdlib.h>

void	*ft_calloc(size_t nitems, size_t size)
{
	void	*ptr;

	ptr = malloc(nitems * size);
	if (ptr == NULL)
		return (ptr);
	ft_bzero(ptr, size * nitems);
	return (ptr);
}
